#include "../includes/ft_printf.h"

void	ft_bzero(void *s, size_t n)
{
	char	*cs;
	size_t	i;

	i = 0;
	cs = (char *)s;
	while (i < n)
	{
		cs[i] = 0;
		i++;
	}
}
